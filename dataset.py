"""
Dataset and DataLoader utilities for Multimodal Sentiment Analysis.

Each sample in the dataset is expected to be a dict with:
    text        : str  – original social-media post text
    image_path  : str  – path to the original image
    gen_text    : str  – text generated by Stable Diffusion captioning the image
    gen_image_path: str – path to the image generated by SD from the text
    label       : int  – sentiment label  0=negative, 1=neutral, 2=positive
"""

import os
from typing import Dict, List, Optional

import torch
from PIL import Image
from torch.utils.data import Dataset, DataLoader
from transformers import BertTokenizer, ViTFeatureExtractor


LABEL_MAP = {"negative": 0, "neutral": 1, "positive": 2}


class MultimodalSentimentDataset(Dataset):
    """
    Loads text+image pairs along with their cross-modal Stable-Diffusion
    generated counterparts (pre-generated and stored on disk).
    """

    def __init__(
        self,
        samples: List[Dict],
        tokenizer: BertTokenizer,
        feature_extractor: ViTFeatureExtractor,
        max_text_len: int = 128,
    ):
        """
        Args:
            samples: list of dicts with keys
                     {text, image_path, gen_text, gen_image_path, label}
            tokenizer:         HuggingFace BERT tokenizer
            feature_extractor: HuggingFace ViT feature extractor
            max_text_len:      maximum token length for BERT
        """
        self.samples           = samples
        self.tokenizer         = tokenizer
        self.feature_extractor = feature_extractor
        self.max_text_len      = max_text_len

    def __len__(self) -> int:
        return len(self.samples)

    def _encode_text(self, text: str) -> Dict[str, torch.Tensor]:
        enc = self.tokenizer(
            text,
            max_length=self.max_text_len,
            padding="max_length",
            truncation=True,
            return_tensors="pt",
        )
        return {
            "input_ids":      enc["input_ids"].squeeze(0),       # (L,)
            "attention_mask": enc["attention_mask"].squeeze(0),  # (L,)
        }

    def _encode_image(self, image_path: str) -> torch.Tensor:
        image = Image.open(image_path).convert("RGB")
        enc   = self.feature_extractor(images=image, return_tensors="pt")
        return enc["pixel_values"].squeeze(0)   # (3, 224, 224)

    def __getitem__(self, idx: int) -> Dict[str, torch.Tensor]:
        sample = self.samples[idx]

        text_enc   = self._encode_text(sample["text"])
        gt_enc     = self._encode_text(sample["gen_text"])
        img_tensor = self._encode_image(sample["image_path"])
        gen_tensor = self._encode_image(sample["gen_image_path"])

        label = (
            sample["label"]
            if isinstance(sample["label"], int)
            else LABEL_MAP[sample["label"].lower()]
        )

        return {
            "text_ids":          text_enc["input_ids"],
            "text_mask":         text_enc["attention_mask"],
            "image_pixels":      img_tensor,
            "gen_text_ids":      gt_enc["input_ids"],
            "gen_text_mask":     gt_enc["attention_mask"],
            "gen_image_pixels":  gen_tensor,
            "label":             torch.tensor(label, dtype=torch.long),
        }


def build_dataloaders(
    train_samples: List[Dict],
    val_samples:   List[Dict],
    test_samples:  Optional[List[Dict]] = None,
    batch_size:    int = 16,
    max_text_len:  int = 128,
    num_workers:   int = 4,
) -> Dict[str, DataLoader]:
    """
    Convenience factory that builds train / val / (optional) test DataLoaders.
    """
    tokenizer         = BertTokenizer.from_pretrained("bert-base-uncased")
    feature_extractor = ViTFeatureExtractor.from_pretrained("google/vit-base-patch16-224")

    def make_loader(samples, shuffle):
        ds = MultimodalSentimentDataset(
            samples, tokenizer, feature_extractor, max_text_len
        )
        return DataLoader(
            ds,
            batch_size=batch_size,
            shuffle=shuffle,
            num_workers=num_workers,
            pin_memory=True,
        )

    loaders = {
        "train": make_loader(train_samples, shuffle=True),
        "val":   make_loader(val_samples,   shuffle=False),
    }
    if test_samples:
        loaders["test"] = make_loader(test_samples, shuffle=False)

    return loaders
